    {
        public RecvUnionCloseWindow()
            : base((ushort) AreaPacketId.recv_union_close_window_r, ServerType.Area)
        {
        }

        protected override IBuffer ToBuffer()
        {
            IBuffer res = BufferProvider.Provide();
            res.WriteInt32(0);
            return res;
        }
    }
